SELECT 	  DET.COD_DETALLE
		, DET.COD_PERSONA
        , PER.NOMBRES
        , PCP.COD_PERSONA_CONDICION
        , T_PER.PERSONA_CONDICION
        , DET.COD_PROCESO
        , PROC.PROCESO
        , DET.COD_DETALLE_TIPO
        , T_DET.DETALLE_TIPO
        , DET.VALOR
        , DET.OBSERVACIONES
		/*,DET.*, PER.*, PROC.*, T_DET.*, PCP.*, T_PER.* */
FROM 	bsd_detalle AS DET, 
		bsd_persona AS PER, 
		bsd_proceso AS PROC,
        ref_detalle_tipo AS T_DET,
        rel_pers_cond_proc AS PCP,
        ref_persona_condicion AS T_PER
WHERE DET.COD_PERSONA = PER.COD_PERSONA
  AND DET.COD_PROCESO = PROC.COD_PROCESO
  AND DET.COD_DETALLE_TIPO = T_DET.COD_DETALLE_TIPO
  AND DET.COD_PERSONA = PCP.COD_PERSONA
  AND DET.COD_PROCESO = PCP.COD_PROCESO
  AND PCP.COD_PERSONA_CONDICION = T_PER.COD_PERSONA_CONDICION
;